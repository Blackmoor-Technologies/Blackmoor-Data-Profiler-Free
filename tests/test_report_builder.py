def test_build_markdown_report_safely_handles_pipe_characters():
	profile = {
		"row_count": 1,
		"column_count": 1,
		"columns": [
			{
				"column_name": "name|with|pipe",
				"inferred_dtype": "object",
				"missing_count": 0,
				"missing_percent": 0.0,
				"distinct_count": 1,
				"sample_values": ["A|B"],
			}
		],
	}
	quality_checks = {
		"duplicate_row_count": 0,
		"duplicate_row_percent": 0.0,
		"findings": [
			{
				"check_name": "Possible Date Column",
				"severity": "info",
				"column_name": "date|col",
				"message": "Contains A|B patterns",
			}
		],
	}

	report = build_markdown_report(
		profile=profile,
		quality_checks=quality_checks,
		file_name="pipe|file.csv",
		file_type="CSV",
	)

	assert "pipe\\|file.csv" in report
	assert "name\\|with\\|pipe" in report
	assert "A\\|B" in report
	assert "date\\|col" in report
	assert "Contains A\\|B patterns" in report
	assert "| name\\|with\\|pipe | object | TODO: Describe the business meaning of this field. | 0.0% | 1 | A\\|B |" in report
