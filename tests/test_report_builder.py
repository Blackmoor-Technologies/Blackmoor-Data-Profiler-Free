from profiler.report_builder import build_markdown_report


def _sample_profile() -> dict:
	return {
		"row_count": 3,
		"column_count": 2,
		"columns": [
			{
				"column_name": "name",
				"inferred_dtype": "object",
				"missing_count": 0,
				"missing_percent": 0.0,
				"distinct_count": 3,
				"sample_values": ["Alice", "Bob"],
			},
			{
				"column_name": "score",
				"inferred_dtype": "int64",
				"missing_count": 1,
				"missing_percent": 33.33,
				"distinct_count": 2,
				"sample_values": [],
			},
		],
	}


def _sample_quality_checks() -> dict:
	return {
		"duplicate_row_count": 1,
		"duplicate_row_percent": 33.33,
		"findings": [
			{
				"check_name": "Blank-Heavy Column",
				"severity": "warning",
				"column_name": "notes",
				"message": "Column has 66.67% missing values.",
			}
		],
	}


def test_build_markdown_report_includes_title_and_dataset_overview():
	report = build_markdown_report(
		profile=_sample_profile(),
		quality_checks=_sample_quality_checks(),
		file_name="customer_sample.csv",
		file_type="CSV",
	)

	assert "# Blackmoor Data Profile Report" in report
	assert "This report was generated by Blackmoor Data Profiler." in report
	assert "## Report Contents" in report
	assert "- Dataset Overview" in report
	assert "- Data Quality Findings" in report
	assert "## Dataset Overview" in report
	assert "- File name: customer_sample.csv" in report
	assert "- File type: CSV" in report
	assert "- Row count: 3" in report
	assert "- Column count: 2" in report


